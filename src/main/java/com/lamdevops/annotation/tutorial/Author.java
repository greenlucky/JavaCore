package com.lamdevops.annotation.tutorial;

import javax.validation.constraints.NotNull;

public class Author {

    @NotNull
    private String name;

    public Author(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Author{" +
                "name='" + name + '\'' +
                '}';
    }
}
